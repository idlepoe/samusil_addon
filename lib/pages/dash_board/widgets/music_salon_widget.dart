  String _formatDuration(int totalSeconds) {
    final minutes = (totalSeconds / 60).floor();
    final hours = (minutes / 60).floor();

    if (hours > 0) {
      return '${hours}ÏãúÍ∞Ñ ${minutes % 60}Î∂Ñ';
    } else {
